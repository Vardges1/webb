   &nbsp;&nbsp;&nbsp;Ярким проявлением электричества в природе служат молнии, электрическая природа которых была установлена в XVIII веке. Молнии издавна вызывали лесные пожары. По одной из версий, именно молнии привели к первоначальному синтезу аминокислот и появлению жизни на земле (Эксперимент Миллера — Юри и Теория Опарина — Холдейна). Атмосфера Земли представляет собой гигантский конденсатор, нижняя обкладка которого (земная поверхность) заряжена отрицательно, а верхняя обкладка (верхние слои атмосферы до высоты 50 км) положительно. Разность потенциалов между поверхностью Земли и верхними слоями атмосферы составляет 400 кВ, вблизи поверхности Земли существует постоянное электрическое поле напряжённостью 100 В/м.

  <br> &nbsp;&nbsp;&nbsp;Для процессов в нервной системе человека и животных решающее значение имеет зависимость пропускной способности клеточной мембраны для ионов натрия от потенциала внутриклеточной среды. После повышения напряжения на клеточной мембране натриевый канал открывается на время порядка 0,1 — 1,0 мс., что приводит к скачкообразному росту напряжения, затем разность потенциалов на мембране снова возвращается к своему первоначальному значению. Описанный процесс кратко называется нервным импульсом. В нервной системе животных и человека информацию от одной клетки к другой передают нервные импульсы возбуждения длительностью около 1 мс. Нервное волокно представляет собой цилиндр, наполненный электролитом. Сигнал возбуждения передаётся без уменьшения амплитуды вследствие эффекта кратковременного увеличения проницаемости мембраны для ионов натрия[17].

<br> &nbsp;&nbsp;&nbsp;Многие рыбы используют электричество для защиты и поиска добычи под водой. Южноамериканский электрический угорь способен генерировать электрические разряды напряжением до 500 вольт. Мощность разрядов электрического ската может достигать 500 Вт. Акулы, миноги, некоторые сомообразные используют электричество для поиска добычи. Электрический орган рыб работает с частотой несколько сотен герц и создаёт напряжение в несколько вольт. Электрическое поле улавливается электрорецепторами. Находящиеся в воде предметы искажают электрическое поле. По этим искажениям рыбы легко ориентируются в мутной воде
