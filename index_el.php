          &nbsp;&nbsp;&nbsp;Электрический заряд — это свойство тел (количественно характеризуемое физической величиной того же названия), проявляющееся, прежде всего, в способности создавать вокруг себя электрическое поле и посредством него оказывать воздействие на другие заряженные (то есть обладающие электрическим зарядом) тела. Электрические заряды разделяют на положительные и отрицательные (выбор, какой именно заряд назвать положительным, а какой отрицательным, считается в науке чисто условным, однако этот выбор уже исторически сделан и теперь — хоть и условно — за каждым из зарядов закреплён вполне определённый знак). Тела, заряженные зарядом одного знака, отталкиваются, а противоположно заряженные — притягиваются. При движении заряженных тел (как макроскопических тел, так и микроскопических заряженных частиц, переносящих электрический ток в проводниках) возникает магнитное поле и, таким образом, имеют место явления, позволяющие установить родство электричества и магнетизма (электромагнетизм) (Эрстед, Фарадей, Максвелл). В структуре материи электрический заряд как свойство тел восходит к заряженным элементарным частицам, например, электрон и антипротон имеют отрицательный заряд, а протон и позитрон — положительный.<br>

 &nbsp;&nbsp;&nbsp;Наиболее общая фундаментальная наука, изучающая электрические заряды, их взаимодействие и поля, ими порождаемые и действующие на них (то есть практически полностью покрывающая тему электричества, за исключением таких деталей, как электрические свойства конкретных веществ, как то электропроводность (и т. п.) — это электродинамика. Квантовые свойства электромагнитных полей, заряженных частиц (и т. п.) изучаются наиболее глубоко квантовой электродинамикой, хотя часть из них может быть объяснена более простыми квантовыми теориями.
